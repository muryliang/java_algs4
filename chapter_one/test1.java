import edu.princeton.cs.algs4.StdOut;
public class test1 {
    public static void main(String[] args) {
        StdOut.printf("result is %d\n", (0 + 15) / 2);
        StdOut.printf("result is %f\n", 2.0e-6 * 100000000.1);
        StdOut.printf("result is %b\n", true && false || true && true);
        StdOut.printf("result is %f\n", (1 + 2.236) / 2);
        StdOut.printf("result is %f\n", 1 + 2 + 3 + 4.0);
        StdOut.printf("result is %B\n", 4.1 >= 4);
        StdOut.printf("result is %s\n", 1 + 2 + "3");
        StdOut.printf("result is %s\n", 1 + 2 + "3" + 4 + 5);
    }
}
    
